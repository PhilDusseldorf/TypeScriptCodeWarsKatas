import { decodeBits, decodeMorse } from './morseCode2';
import { assert } from 'chai';

describe('Example from description', () => {
  it('Example from description', () => {
    let bits =
      '1100110011001100000011000000111111001100111111001111110000000000000011001111110011111100111111000000110011001111110000001111110011001100000011';
    assert.equal(decodeMorse(decodeBits(bits)), 'HEY JUDE');
    bits =
      '0000000001100110011001100000011000000111111001100111111001111110000000000000011001111110011111100111111000000110011001111110000001111110011001100000011';
    assert.equal(decodeMorse(decodeBits(bits)), 'HEY JUDE');
    bits =
      '1100110011001100000011000000111111001100111111001111110000000000000011001111110011111100111111000000110011001111110000001111110011001100000011000';
    assert.equal(decodeMorse(decodeBits(bits)), 'HEY JUDE');
    bits = '0000000000000000000000000000110011001100110000000000';
    assert.equal(decodeMorse(decodeBits(bits)), 'H');
    bits = '000000111111000000000000001100111111001111110011111100000000';
    assert.equal(decodeMorse(decodeBits(bits)), 'T J');
  });
});
